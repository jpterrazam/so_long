#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (s)
	{
		while (*s && (*s != (unsigned char) c))
			s++;
		if (*s == (unsigned char) c)
			return ((char *) s);
	}
	return (NULL);
}
